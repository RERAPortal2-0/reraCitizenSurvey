package in.gov.rera.citizen.survey.model.transaction;

public class FlatModelTransactionModel {

	private String flatId;
	private String flatNo;
	private String flatName;
	private String flatStatus;
	private String alloteeId;
	private String alloteeName;
	private String alloteeType;

	public String getFlatId() {
		return flatId;
	}

	public void setFlatId(String flatId) {
		this.flatId = flatId;
	}

	public String getFlatNo() {
		return flatNo;
	}

	public void setFlatNo(String flatNo) {
		this.flatNo = flatNo;
	}

	public String getFlatName() {
		return flatName;
	}

	public void setFlatName(String flatName) {
		this.flatName = flatName;
	}

	public String getFlatStatus() {
		return flatStatus;
	}

	public void setFlatStatus(String flatStatus) {
		this.flatStatus = flatStatus;
	}

	public String getAlloteeId() {
		return alloteeId;
	}

	public void setAlloteeId(String alloteeId) {
		this.alloteeId = alloteeId;
	}

	public String getAlloteeName() {
		return alloteeName;
	}

	public void setAlloteeName(String alloteeName) {
		this.alloteeName = alloteeName;
	}

	public String getAlloteeType() {
		return alloteeType;
	}

	public void setAlloteeType(String alloteeType) {
		this.alloteeType = alloteeType;
	}

}
